// al cargar la página, obetemos lo del localStorage
document.addEventListener("DOMContentLoaded", () => {
  // Busca si hay algo guardado, si no, usa 'es' por defecto
  const idiomaGuardado = localStorage.getItem("language") || "es";

  // Sincroniza el select visualmente
  languageSelector.value = idiomaGuardado;

  // Aplica el idioma
  cambiarIdioma(idiomaGuardado);
});

const languageSelector = document.querySelector(".select-idioma");

languageSelector.addEventListener("change", (event) => {
  const idiomaSeleccionado = event.target.value; // 'es' o 'en'

  // guardamos el cambio de idioma en LocalStorage
  localStorage.setItem("language", idiomaSeleccionado);
  cambiarIdioma(idiomaSeleccionado);
});

function cambiarIdioma(lenguaje) {
  // Selecciona todos los elementos que tengan el atributo data-translate
  const elementosATraducir = document.querySelectorAll("[data-translate]");

  elementosATraducir.forEach((elemento) => {
    const clave = elemento.getAttribute("data-translate"); // ej: "titulo_principal"

    // Busca la traducción en el objeto y la aplica
    if (traducciones[lenguaje][clave]) {
      elemento.innerHTML = traducciones[lenguaje][clave];
    }
  });
}
